//Program to perform operation with different arithmetic operators.
fun main()
{
    var num1 = 15
    var num2 = 12
    println("Addition of $num1 and $num2 : "+(num1+num2))
    println("Subtraction of $num1 and $num2 : "+(num1-num2))
    println("Multiplication of $num1 and $num2 : "+(num1*num2))
    println("Division of $num1 and $num2 : "+(num1/num2))
    println("Modulus of $num1 and $num2 : "+(num1%num2))
}